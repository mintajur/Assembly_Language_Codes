.MODEL SMALL
.STACK 100H
.DATA
INPUT DB 'TYPE A CHARACTER: $'
AS1 DB 10,13,'THE ASCII CODE OF $'
AS2 DB ' IN BINARY IS: $'
ONE DB 10,13,'THE NUMBER OF 1 BITS IS: $'
TWO DB 10,13,'THE NUMBER OF 0 BITS IS: $'
A DB 30H
B DB 30H
.CODE 

 

MAIN PROC
    CALL TASK
    
MAIN ENDP 
    
TASK PROC
MOV AX,@DATA
MOV DS,AX
    
MOV AH,9
LEA DX,INPUT
INT 21H

 

MOV AH,1
INT 21H
MOV BL,AL

 

MOV AH,9
LEA DX,AS1
INT 21H

 

MOV AH,2
MOV DL,BL
INT 21H

 

MOV AH,9
LEA DX,AS2
INT 21H 

 

MOV CX,8
PRINT:
ROL BL,1
JNC ZERO 

 

MOV AH,2
MOV DL,'1'
INT 21H
INC A
JMP NEXT 

 

ZERO:
MOV AH,2
MOV DL,'0'
INT 21H
INC B 

 

NEXT:
LOOP PRINT 

 

MOV AH,9
LEA DX,ONE
INT 21H

 

MOV AH,2
MOV DL,A   ;1
INT 21H

 

MOV AH,9
LEA DX,TWO
INT 21H

 

MOV AH,2
MOV DL,B   ;0
INT 21H   
;RET
TASK ENDP